def get_vendor_from_oui(mac_address: str) -> str:
    """Get vendor from MAC address using OUI database"""
    try:
        # Clean and normalize MAC address
        oui = mac_address.replace(':', '').replace('-', '').replace('.', '').upper()[:6]
        formatted_oui = f"{oui[:2]}:{oui[2:4]}:{oui[4:6]}"
        
        return COMMON_OUIS.get(formatted_oui, "Unknown")
    except Exception:
        return "Unknown"

def get_device_type_from_vendor(vendor: str) -> str:
    """Determine device type from vendor name"""
    if vendor == "Unknown":
        return "Unknown"
        
    vendor_lower = vendor.lower()
    for device_type, patterns in DEVICE_TYPE_PATTERNS.items():
        if any(pattern in vendor_lower for pattern in patterns):
            return device_type
            
    return "Computer"  # Default fallback
